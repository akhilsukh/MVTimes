    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            //Returning the current tab
            switch (position){
                case 0:
                    TabEditClasses tab1 = new TabEditClasses();
                    return tab1;
                case 1:
                    TabEditTeachersRooms tab2 = new TabEditTeachersRooms();
                    return tab2;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 2;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Classes";
                case 1:
                    return "Teachers/Rooms";
            }
            return null;
        }
    }
    @Override
    public void onBackPressed() {
        super.onBackPressed();
        finish();
    }
