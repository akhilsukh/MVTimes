public class ExampleItem {
    private String mTitle;
    private String mNote;

    public ExampleItem(String title, String note){
        mTitle = title;
        mNote = note;
    }

    public String getmTitle(){
        return mTitle;
    }
    public String getmDate(){
        return mNote;
    }


}
